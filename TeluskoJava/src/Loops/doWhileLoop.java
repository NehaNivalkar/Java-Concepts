package Loops;

public class doWhileLoop {

	public static void main(String[] args) {
		 int i =6;
		  do {
			  System.out.println("Hardik" + i);
			  i++;
		  }
		  while (i<=4);

	}

}
 //note: in while loop it will only run when the condition is true i.e while (i<=4); but here in the above code we have set the variable value as 6, which is greater than 4, so in do while loop it will run once and then check the condition.
